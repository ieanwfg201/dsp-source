package models.uiutils;


public class Path {

	private String label;
	
	private String path;

	
	public Path(String label, String path) {
		super();
		this.label = label;
		this.path = path;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
	
	 
	
}
