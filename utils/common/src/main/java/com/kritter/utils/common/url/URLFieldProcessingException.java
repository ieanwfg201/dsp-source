package com.kritter.utils.common.url;

public class URLFieldProcessingException extends Exception
{
    public URLFieldProcessingException()
    {
        super();
    }

    public URLFieldProcessingException(String message)
    {
        super(message);
    }

    public URLFieldProcessingException(String message, Throwable cause)
    {
        super(message,cause);
    }

    public URLFieldProcessingException(Throwable cause)
    {
        super(cause);
    }
}

